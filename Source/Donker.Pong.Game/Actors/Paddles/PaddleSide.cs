namespace Donker.Pong.Game.Actors.Paddles
{
    /// <summary>
    /// Specifies the side where a paddle is located.
    /// </summary>
    public enum PaddleSide
    {
        /// <summary>
        /// The side of the paddle is undetermined.
        /// </summary>
        Undetermined = 0,
        /// <summary>
        /// The paddle is on the left side.
        /// </summary>
        Left = 1,
        /// <summary>
        /// The paddle is on the right side.
        /// </summary>
        Right = 2
    }
}